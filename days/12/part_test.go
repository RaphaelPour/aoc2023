func TestGoalReached(t *testing.T) {
	for _, testCase := range []struct {
		name     string
		input    string
		goal     []int
		expected bool
	}{
		{name: "No damage", input: "...", goal: []int{}, expected: true},
		{name: "No damage but goal wants one", input: "...", goal: []int{1}, expected: false},
		{name: "Only damage", input: "###", goal: []int{3}, expected: true},
		{name: "Only damage but not expected", input: "###", goal: []int{}, expected: false},
		{name: "Only damage but goal wants two groups", input: "###", goal: []int{1, 1}, expected: false},
		{name: "1", input: ".#.#.", goal: []int{1, 1}, expected: true},
		{name: "? fails always 1", input: "?", goal: []int{1}, expected: false},
		{name: "? fails always 2", input: ".?", goal: []int{1}, expected: false},
		{name: "? fails always 3", input: ".#?", goal: []int{1}, expected: false},
		{name: "? fails always 4", input: "..#.#.#.#?", goal: []int{1, 2, 3, 4}, expected: false},
		{name: "? fails always 5", input: "???", goal: []int{1}, expected: false},
	} {
		t.Run(testCase.name, func(t *testing.T) {
			require.Equal(t, testCase.expected, GoalReached(testCase.input, testCase.goal))
		})
	}
}

